// Helper functions for project management
export const getFeaturedProjects = () => {
  return projectsData.filter(project => project.featured).slice(0, 3);
};

export const getProjectsByCategory = (category) => {
  return projectsData.filter(project => project.category === category);
};

export const getRandomProjects = (count = 3) => {
  return projectsData.sort(() => Math.random() - 0.5).slice(0, count);
};

export const getProjectBySlug = (slug) => {
  return projectsData.find(project => project.slug === slug);
};

export const getProjectsByDifficulty = (difficulty) => {
  return projectsData.filter(project => project.difficulty === difficulty);
};
